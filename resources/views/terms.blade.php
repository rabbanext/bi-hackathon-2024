					<h3 class="fst-italic mb-4">
						Syarat dan Ketentuan Lomba BI-OJK Hackathon 2025
					</h3>
					<h5 class="pt-4">I. Pendahuluan</h5>
					<p>
						BI-OJK Hackathon 2025 (<b>“Hackathon”</b>), sebagai bagian dari rangkaian acara Festival Ekonomi Digital Indonesia (<b>“FEKDI”</b>) 2025 dan Indonesia Fintech Summit & Expo (<b>“IFSE”</b>) 2025 yang diselenggarakan oleh Bank Indonesia dan OJK (<b>“Penyelenggara”</b>), merupakan ajang kompetisi intelektual yang diikuti oleh industri, akademisi, dan/atau individu selaku inovator, pengembang, dan pemikir kreatif (<b>“Peserta”</b>) untuk memanfaatkan teknologi khususnya artificial intelligence atau machine learning (<b>“AI/ML”</b>) dan block chain dalam menyelesaikan berbagai permasalahan. Hackathon memberikan kesempatan bagi setiap individu untuk menunjukkan kemampuan dalam mengatasi tantangan, mengembangkan solusi, dan bekerja sama dalam tim. Selain itu, Hackathon juga menjadi platform pembelajaran, pertumbuhan, dan jaringan bagi para pihak dengan beragam latar belakang industri. Melalui kolaborasi dan inovasi, kami yakin bahwa kita mampu memanfaatkan AI/ML dan block chain untuk membantu mengatasi berbagai tantangan yang dihadapi oleh dunia saat ini.
					</p>

					<h5 class="pt-4">II. Tujuan</h5>
					<p>
						Penyelenggaraan Hackathon bertujuan untuk mendorong pengembangan pemanfaatan AI/ML dan block chain oleh industri, akademisi, dan individu guna mendukung perluasan ekonomi dan keuangan digital. Pemanfaatan AI/ML dan block chain tidak hanya diharapkan dapat menyelesaikan permasalahan yang disediakan namun juga memiliki potensi model bisnis yang mampu diimplementasikan dan dikembangkan ke depan. 
					</p>

					<h5 class="pt-4">III. Peserta</h5>
					<p>
						Hackathon dirancang untuk para inovator, pengembang, dan pemikir kreatif serta profesional atau perorangan yang berpengalaman di bidang data, AI/ML, dan block chain yang ingin menunjukkan keahlian mereka dan berkontribusi dalam pengembangan solusi inovatif menuju integrasi ekonomi digital dan keuangan Indonesia. 
					</p>
					<h6>Persyaratan Umum Peserta</h6>
					<ol>
						<li>Peserta merupakan Warga Negara Indonesia.</li>
						<li>Peserta dapat membentuk tim dengan jumlah maksimal 4 anggota.</li>
						<li>Peserta bukan merupakan pegawai dari organisasi yang tergabung sebagai panitia atau penyelenggara.</li>
					</ol>
					<h6>Persyaratan Khusus Peserta</h6>
					<p>Kategori mahasiswa:  Seluruh anggota tim merupakan mahasiswa aktif program  S1 dan S2 dari perguruan tinggi di Indonesia maupun luar negeri, dibuktikan dengan kartu tanda mahasiswa (KTM) atau surat keterangan aktif kuliah.</p>
					<p>Kategori Profesional: Terbuka bagi tim yang terdiri dari lulusan perguruan tinggi, pelaku industri, startup, dosen, peneliti, ASN, freelancer maupun pekerja profesional lainnya.</p>
					
